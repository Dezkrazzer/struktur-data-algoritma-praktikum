package pertemuan_3;

public class App {

    public static void main(String[] args) throws Exception {
        int i, n = 8;
        for (i = 1; i <= n; i++) {
            System.out.println("Hello, world!");
        }

        // int i = O (1)
        // int n = O (1)
        // for (i = 1; i <= n; i++) = O (n)
        // i = 1 = O (1)
        // i <= n = O (1)
        // i++ = O (1)
        // total = O (1) + O (n) = O (n)
    }
}
